package model;

/**
 *
 * @author omar
 */
public class teacher {
    private int id;
    private String name,depart;

    public teacher(int id, String name, String depart) {
        this.id = id;
        this.name = name;
        this.depart = depart;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDepart() {
        return depart;
    }

    public void setDepart(String depart) {
        this.depart = depart;
    }
    
    
}
