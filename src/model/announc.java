package model;

/**
 *
 * @author omar
 */
public class announc {
    private String title,text,image,date;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public announc(String title, String text, String image, String date) {
        this.title = title;
        this.text = text;
        this.image = image;
        this.date = date;
    }
}
